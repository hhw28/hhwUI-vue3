<template>
  <div class="hhw-tabs">
    <div class="hhw-tabs-nav" ref="container">
      <div class="hhw-tabs-nav-item"
           v-for="(title, index) in titles"
           :ref="el => { if (title===selected) selectedItem = el }"
           :key="index"
           :class="{selected: selected === title}"
           @click="changeSelect(title)"
      >{{title}}
      </div>
      <div class="hhw-tabs-nav-indicator" ref="indicator"></div>
    </div>
    <div class="hhw-tabs-content">
      <component :is="current" :key="current.props.title"/>
    </div>
  </div>
</template>
<script lang="ts">
import Tab from './Tab.vue'
import {computed, ref, onMounted, watchEffect} from 'vue'

export default {
  components: {Tab},
  props: {
    selected: {
      type: String,
      default: ''
    }
  },
  setup(props, context) {
    const defaults = context.slots.default()
    defaults.forEach((tag) => {
      if (tag.type !== Tab) {
        throw new Error('Tabs 子标签必须是 Tab')
      }
    })

    const titles = defaults.map((tag) => {
      return tag.props.title
    })

    const current = computed(() => {
      return defaults.find(tag => tag.props.title === props.selected)
    })

    const changeSelect = (title: string) => {
      context.emit('update:selected', title)
    }

    const selectedItem = ref<HTMLDivElement>(null)
    const indicator = ref<HTMLDivElement>(null)
    const container = ref<HTMLDivElement>(null)

    onMounted(() => {
      watchEffect(() => {
        const {width} = selectedItem.value.getBoundingClientRect()
        indicator.value.style.width = width + 'px'

        const {left: left1} = container.value.getBoundingClientRect()
        const {left: left2} = selectedItem.value.getBoundingClientRect()
        const left = left2 - left1
        indicator.value.style.left = left + 'px'
      },{
        flush: 'post'
      })
    })

    return {
      defaults,
      titles,
      current,
      changeSelect,
      selectedItem,
      indicator,
      container
    }
  },
};
</script>
